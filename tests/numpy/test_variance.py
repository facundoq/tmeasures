    m.update_all(x)
    print(m.var(),m.mean())
    assert np.sum(m.var()<0)==0

    m = RunningMeanAndVarianceWelford()
    for i in range(x.shape[0]):
        m.update(x[i,])
    print(m.var(), m.mean())
    assert np.sum(m.var() < 0) == 0

    print(x.var(axis=0),x.mean(axis=0))
